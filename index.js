function checkSex(v) {
  if (v == 1 || v === 'm' || v === 'M' || v === 'ช' || v === 'ชาย') v = 'ชาย';
  else if (v == 2 || v === 'f' || v === 'F' || v === 'ญ' || v === 'หญิง') v = 'หญิง';
  else v = null;
  return v;
}

function checkBloodGroup(v) {
  if (v == 1) v = 'A';
  else if (v == 2) v = 'B';
  else if (v == 3) v = 'AB';
  else if (v == 4) v = 'O';
  else if (v == 9) v = 'ไม่ทราบ';
  else v = null;
  return v;
}

function checkRhGroup(v) {
  if (v == 1) v = 'positive';
  else if (v == 2) v = 'negative';
  else v = null
  return v
}

